#include <stdio.h>

int main(void) {
    int a = 10;
    int b = 20;

    printf("a = %d\n", a);
    printf("b = %d\n\n", b);

    printf("a == b : %d\n", a == b);
    printf("a != b : %d\n", a != b);
    printf("a < b : %d\n", a < b);
    printf("a <= b : %d\n", a <= b);
    printf("a > b : %d\n", a > b);
    printf("a >= b : %d\n", a >= b);

    return 0;
}
